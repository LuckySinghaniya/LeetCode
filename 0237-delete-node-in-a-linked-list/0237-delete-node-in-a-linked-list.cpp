/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    void deleteNode(ListNode* node) {
        struct ListNode* temp=new ListNode();
        temp=node->next->next;
        node->val=node->next->val;
        node->next->next=nullptr;
        node->next=temp;
        
    }
};

